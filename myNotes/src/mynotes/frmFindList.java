    public frmFindList(globalDataArea m) throws SQLException {
        initComponents();
        gDatos = m;
        

        DefaultListModel dlm = new DefaultListModel();
        this.jList1.setModel(dlm);
        dlm.clear();

        /*
            Se conecta a BD para traer datos
        */
        oracleDB conn = new oracleDB();
        conn.setDbName("oratest");
        conn.setDbUser("notes");
        conn.setDbPass("notes01");
        conn.setHostIp("oradb01");
        conn.setDbPort("1521");
        
        conn.conectar();
        
        if (conn.getConnStatus()) {
            String vSQL="select catID, catDesc from nt_category where lower(catDesc) like lower('%"+gDatos.getCmdMenu()+"%')";
            ResultSet rs = conn.consultar(vSQL);
            if (rs!=null) {
                while (rs.next()) {
                    listCatID.add(rs.getString("catID"));
                    dlm.addElement(rs.getString("catDesc"));
                }
            }
            conn.closeConexion();
        }
        
    }

