package org.model;

public class ExpTableParam {
	
	private String etbID;
	private String etbFieldName;
	private String etbFieldLabel;
	private String etbFieldType;
	private int etbOrder;
	private String etbDataType;
	
	//Getter and Setter
	
	public String getEtbID() {
		return etbID;
	}
	public void setEtbID(String etbID) {
		this.etbID = etbID;
	}
	public String getEtbFieldName() {
		return etbFieldName;
	}
	public void setEtbFieldName(String etbFieldName) {
		this.etbFieldName = etbFieldName;
	}
	public String getEtbFieldLabel() {
		return etbFieldLabel;
	}
	public void setEtbFieldLabel(String etbFieldLabel) {
		this.etbFieldLabel = etbFieldLabel;
	}
	public String getEtbFieldType() {
		return etbFieldType;
	}
	public void setEtbFieldType(String etbFieldType) {
		this.etbFieldType = etbFieldType;
	}
	public int getEtbOrder() {
		return etbOrder;
	}
	public void setEtbOrder(int etbOrder) {
		this.etbOrder = etbOrder;
	}
	public String getEtbDataType() {
		return etbDataType;
	}
	public void setEtbDataType(String etbDataType) {
		this.etbDataType = etbDataType;
	}
	
}
