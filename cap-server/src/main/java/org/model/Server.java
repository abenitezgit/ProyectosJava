package org.model;

public class Server {
	
	private String serverID;
	private String serverDesc;
	private String serverEnable;
	private String serverHostName;
	private String serverIP;
	private String serverDomain;
	private String serverCliID;
	private String serverRoleMain;
	
	//Getter and Setter
	
	public String getServerID() {
		return serverID;
	}
	public void setServerID(String serverID) {
		this.serverID = serverID;
	}
	public String getServerDesc() {
		return serverDesc;
	}
	public void setServerDesc(String serverDesc) {
		this.serverDesc = serverDesc;
	}
	public String getServerEnable() {
		return serverEnable;
	}
	public void setServerEnable(String serverEnable) {
		this.serverEnable = serverEnable;
	}
	public String getServerHostName() {
		return serverHostName;
	}
	public void setServerHostName(String serverHostName) {
		this.serverHostName = serverHostName;
	}
	public String getServerIP() {
		return serverIP;
	}
	public void setServerIP(String serverIP) {
		this.serverIP = serverIP;
	}
	public String getServerDomain() {
		return serverDomain;
	}
	public void setServerDomain(String serverDomain) {
		this.serverDomain = serverDomain;
	}
	public String getServerCliID() {
		return serverCliID;
	}
	public void setServerCliID(String serverCliID) {
		this.serverCliID = serverCliID;
	}
	public String getServerRoleMain() {
		return serverRoleMain;
	}
	public void setServerRoleMain(String serverRoleMain) {
		this.serverRoleMain = serverRoleMain;
	}
}
